    private void cusEmailAddresscusEmailfocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cusEmailAddresscusEmailfocusLost
        // TODO add your handling code here:
        final String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";

        String email = cusEmailAddress.getText();

        if (!email.matches(EMAIL_PATTERN)) {
            cusEmailAddress.setForeground(Color.red);
        };
    }//GEN-LAST:event_cusEmailAddresscusEmailfocusLost

