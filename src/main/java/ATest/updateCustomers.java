    private void cusContactNumbercontactNumberFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cusContactNumbercontactNumberFocusLost
        // TODO add your handling code here:
        final String CONTACT_NUMBER_PATTERN = "0\\d{9}";
        final String CONTACT_NUMBER_PATTERN2 = "+94\\d{9}";

        String contact = cusContactNumber.getText();
        boolean contactCheck = contact.matches(CONTACT_NUMBER_PATTERN)
        || contact.matches(CONTACT_NUMBER_PATTERN2);

        if (!contactCheck) {
            cusContactNumber.setForeground(Color.red);
        };
    }//GEN-LAST:event_cusContactNumbercontactNumberFocusLost

