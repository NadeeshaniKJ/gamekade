    private void cusEmailfocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cusEmailfocusLost
        // TODO add your handling code here:
        final String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";

        String email = cusEmailAddress.getText();

        if (!email.matches(EMAIL_PATTERN)) {
            cusEmailAddress.setForeground(Color.red);
        };
    }//GEN-LAST:event_cusEmailfocusLost

    private void NICFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_NICFocusLost
        // TODO add your handling code here:
        final String NIC_PATTERN_1 = "\\d{9}+V";
        final String NIC_PATTERN_2 = "\\d{9}+v";
        final String NIC_PATTERN_3 = "\\d{9}+X";
        final String NIC_PATTERN_4 = "\\d{9}+x";
        final String NIC_PATTERN_5 = "\\d{12}";

        String nic = cusNIC.getText();
        boolean nicCheck = nic.matches(NIC_PATTERN_1) || nic.matches(NIC_PATTERN_2)
                || nic.matches(NIC_PATTERN_3) || nic.matches(NIC_PATTERN_4)
                || nic.matches(NIC_PATTERN_5);

        if (!nicCheck) {
            cusNIC.setForeground(Color.red);
        };
    }//GEN-LAST:event_NICFocusLost

